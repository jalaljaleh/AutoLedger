        public static string GetRandomQuranVerse()
        {
            int index = _random.Next(_quranVerses.Count);
            return _quranVerses[index];
        }

        public static string GetTodayZikr()
        {
            DayOfWeek today = DateTime.Now.DayOfWeek;

            switch (today)
            {
                case DayOfWeek.Saturday:
                    return " یَا رَبَّ الْعَالَمِین";
                case DayOfWeek.Sunday:
                    return " یَا ذَا الْجَلَالِ وَ الْإِکْرَام";
                case DayOfWeek.Monday:
                    return " یَا قَاضِیَ الْحَاجَات ";
                case DayOfWeek.Tuesday:
                    return " یَا أَرْحَمَ الرَّاحِمِین ";
                case DayOfWeek.Wednesday:
                    return " یَا حَیُّ یَا قَیُّوم";
                case DayOfWeek.Thursday:
                    return " لَا إِلَهَ إِلَّا الْمَلِکُ الْحَقُّ الْمُبِین ";
                case DayOfWeek.Friday:
                    return " اَللَّهُمَّ صَلِّ عَلَی مُحَمَّدٍ وَ آلِ مُحَمَّد ";
                default:
                    return string.Empty;
            }
        }
    }
